package com.android.carair.api;

public class Notice {

    private String win;
    private String baby;
    private String pregn;
    public String getWin() {
        return win;
    }
    public void setWin(String win) {
        this.win = win;
    }
    public String getBaby() {
        return baby;
    }
    public void setBaby(String baby) {
        this.baby = baby;
    }
    public String getPregn() {
        return pregn;
    }
    public void setPregn(String pregn) {
        this.pregn = pregn;
    }

}
