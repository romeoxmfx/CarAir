package com.android.carair.api;

public class Sleep_period {
    private int sleep_period;
    private int start_hour;
    private int start_min;
    private int end_hour;
    private int end_min;
    
    public int getSleep_period() {
        return sleep_period;
    }
    public void setSleep_period(int sleep_period) {
        this.sleep_period = sleep_period;
    }
    public int getStart_hour() {
        return start_hour;
    }
    public void setStart_hour(int start_hour) {
        this.start_hour = start_hour;
    }
    public int getStart_min() {
        return start_min;
    }
    public void setStart_min(int start_min) {
        this.start_min = start_min;
    }
    public int getEnd_hour() {
        return end_hour;
    }
    public void setEnd_hour(int end_hour) {
        this.end_hour = end_hour;
    }
    public int getEnd_min() {
        return end_min;
    }
    public void setEnd_min(int end_min) {
        this.end_min = end_min;
    }
    
}
