
package com.android.carair.api;

public class Filter {

    private int reset_filter;
    private int left_day;
    private float left_precent;

    public int getReset_filter() {
        return reset_filter;
    }

    public void setReset_filter(int reset_filter) {
        this.reset_filter = reset_filter;
    }

    public int getLeft_day() {
        return left_day;
    }

    public void setLeft_day(int left_day) {
        this.left_day = left_day;
    }

    public float getLeft_precent() {
        return left_precent;
    }

    public void setLeft_precent(float left_precent) {
        this.left_precent = left_precent;
    }

}
