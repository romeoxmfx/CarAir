package com.android.airhelper.api;

public class MobInfo {
	
	private String id;
	private String mac;
	private String model;
	private String cpu;
	private String os;
	private String os_ver;
	private String reso_height;
	private String reso_weight;
	private String type;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMac() {
		return mac;
	}
	public void setMac(String mac) {
		this.mac = mac;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getCpu() {
		return cpu;
	}
	public void setCpu(String cpu) {
		this.cpu = cpu;
	}
	public String getOs() {
		return os;
	}
	public void setOs(String os) {
		this.os = os;
	}
	public String getOs_ver() {
		return os_ver;
	}
	public void setOs_ver(String os_ver) {
		this.os_ver = os_ver;
	}
	public String getReso_height() {
		return reso_height;
	}
	public void setReso_height(String reso_height) {
		this.reso_height = reso_height;
	}
	public String getReso_weight() {
		return reso_weight;
	}
	public void setReso_weight(String reso_weight) {
		this.reso_weight = reso_weight;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
}
